/**
 * Get quote for a specific day (1-30)
 */
export const getQuoteForDay = (day: number): Quote => {
  const index = ((day - 1) % DAILY_QUOTES.length);
  return DAILY_QUOTES[index];
};

/**
 * Get today's quote based on date
 */
export const getTodayQuote = (ramadanStartDate: Date | null): Quote => {
  if (!ramadanStartDate) {
    // Return a random quote if no start date
    const today = new Date();
    const dayOfYear = Math.floor((today.getTime() - new Date(today.getFullYear(), 0, 0).getTime()) / 86400000);
    return DAILY_QUOTES[dayOfYear % DAILY_QUOTES.length];
  }
  
  const today = new Date();
  const start = new Date(ramadanStartDate);
  const diffTime = today.getTime() - start.getTime();
  const diffDays = Math.floor(diffTime / (1000 * 60 * 60 * 24)) + 1;
  
  if (diffDays < 1) {
    // Before Ramadan
    return DAILY_QUOTES[0];
  }
  
  return getQuoteForDay(diffDays);
};
